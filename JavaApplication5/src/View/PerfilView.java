    private void OcultarSenha2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OcultarSenha2ActionPerformed
        // TODO add your handling code here:
        if (OcultarSenha2.isSelected()) {
            // botão ativo: mostrar senha
            Senha.setEchoChar((char) 0);
            OcultarSenha2.setText("Ocultar");
        } else {
            // botão desativado: restaurar echo original
            Senha.setEchoChar(echoOriginal);
            OcultarSenha2.setText("Mostrar");
        }
    }//GEN-LAST:event_OcultarSenha2ActionPerformed

    private void jMenuItem4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem4ActionPerformed
        // TODO add your handling code here:
        if (jMenuItem4.isEnabled()) {
            CadastroView cad = new CadastroView();
            cad.setVisible(false);
            this.dispose();

            Object[] opcoes = {"Não", "Sim"};
            int escolha = JOptionPane.showOptionDialog(null, "Deseja sair?", "Saida", 0, 0, null, opcoes, opcoes[0]);

            if (escolha == 1) {
                LoginView cadastro = new LoginView();

                cadastro.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "retornando...");

                Tela_principal cadastro = new Tela_principal();
                cadastro.setVisible(true);
                this.dispose();
            }
        }
    }//GEN-LAST:event_jMenuItem4ActionPerformed

