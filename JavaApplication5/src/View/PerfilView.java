    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
        Perfil_DTO objlogindto = new Perfil_DTO();

        String nome, usuario, senha;

        nome = Nome_pessoa.getText();
        usuario = Nome_usuario.getText();
        senha = Senha.getText();

        objlogindto.setNome(nome);
        objlogindto.setUsuario(usuario);
        objlogindto.setSenha(senha);

        Perfil_DAO objlogindao = new Perfil_DAO();

        objlogindao.atualizar(objlogindto);

        LoginView login = new LoginView();
        login.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_updateActionPerformed

